{
	var theName = "AUTHORITY=";
	var theCookie = document.cookie+";";
	var start = theCookie.indexOf(theName);
	var end = document.cookie.indexOf(";", start);
	if (end == -1) end = document.cookie.length;
	if(start != -1 && document.cookie.substring(start, end)==theName+0)
		AuthUser='admin';
	else if(start != -1 && document.cookie.substring(start, end)== theName+17)
        AuthUser = 'config_admin';
    else
		AuthUser='user';
}

